);
ALTER TABLE CLASSIFICACOES ADD CONSTRAINT PRIMARY KEY PK_IDECLA (IDECLA);


CREATE TABLE VEICULOS(
  IDEVEI INTEGER NOT NULL,
  NOMVEI VARCHAR(100) NOT NULL,
)
ALTER TABLE VEICULOS ADD CONSTRAINT PRIMARY KEY PK_IDEVEI (IDEVEI);


