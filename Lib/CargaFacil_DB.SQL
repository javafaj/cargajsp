
CREATE TABLE USUARIOS(
  IDEUSU INTEGER NOT NULL,
  NOMUSU VARCHAR(100) NOT NULL,
  FANUSU VARCHAR(100) NOT NULL,
  CGCUSU VARCHAR(30) NOT NULL,
  IESUSU VARCHAR(30) NOT NULL,
  ENDUSU VARCHAR(100) NOT NULL,
  CIDUSU VARCHAR(100) NOT NULL,
  ESTUSU VARCHAR(2) NOT NULL,
  CEPUSU VARCHAR(9) NOT NULL,
  TELUSU INT(11),
  CELUSU INT(11),
  EMAUSU VARCHAR(30) NOT NULL,
);
ALTER TABLE USUARIOS ADD CONSTRAINT PRIMARY KEY PK_IDEUSU (IDEUSU);


CREATE TABLE CLASSIFICACOES(
